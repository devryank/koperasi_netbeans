    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(id != 0) {
            int ok = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus id " + id + " ?", "konfirmasi dialog", JOptionPane.YES_NO_OPTION);    
           if(ok == 0) {
               String sql = "delete from employees where id='"+id+"'";
               try {
                   PreparedStatement stat = conn.prepareStatement(sql);
                   stat.executeUpdate();
                   JOptionPane.showMessageDialog(null, "data berhasil dihapus");
               }
               catch(SQLException e) {
                   JOptionPane.showMessageDialog(null, "data gagal dihapus"+e);
               }
               datatable();
           }
        } else {
            JOptionPane.showMessageDialog(null, "Kamu harus memilih dan mengklik salah satu kolom ID untuk mengubah data");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

